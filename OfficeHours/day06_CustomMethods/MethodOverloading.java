public class MethodOverloading {

    /*
    If A class has multiple same methods.

    Rules:

    - Number of Parameters     OVERLOADING
    add (int A, int B);
    add (int A);

    - Data types of Parameters  OVERLOADING
    add (int A, float B);
    add (int A, int B);

    - Sequence of Parameters    OVERLOADING
    add (int A, float B);
    add (float A, int B);

    - Return Type                INVALID
    int add (int A, float B);    float to integer requires explicit casting
    float add (float A, int B);
     */
}
