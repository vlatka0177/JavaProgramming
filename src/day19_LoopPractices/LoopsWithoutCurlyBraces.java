package day19_LoopPractices;

public class LoopsWithoutCurlyBraces {

    public static void main(String[] args) {

        for(int i = 0; i <5; i++)
            System.out.println("Cydeo"); // Cydeo Cydeo Cydeo Cydeo Cydeo

        for (int i = 0; i < 5; i++) {
            System.out.println("Batch 25"); // Batch 25 Batch 25 Batch 25 Batch 25 Batch 25
        }
    }
}
