package day17_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {

        /*
        For Loop - used when the number of repetitions is known
        While Loop - used when the number of repetitions is not known      */

        if(true){
            System.out.println("Wooden Spoon"); // Executed one time
        }

        System.out.println("------------------------------------------");

        while(true){ // Repeated If Statement. Executed multiple times with the same condition.
            System.out.println("Wooden Spoon");
        }

    }
}
